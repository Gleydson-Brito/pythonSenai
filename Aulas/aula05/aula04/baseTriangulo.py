# CALCULAR A ÁREA DE UM TRIÂNGULO SOMENTE COM OS VALORES DA BASE E ALTURA


print('Vamos agora calcular a área de um triangulo usando Python')

print('Vamos começar digitanto o valor da base: ')

base = float(input('Digite o valor da base \n'))
altura = float(input('Digite o valor da altura \n'))
resposta = (base * altura) / 2


print('A área do trângulo é: ', resposta)

